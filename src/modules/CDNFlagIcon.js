import Flag from "./internal/Flag";

/**
 * Draws flag icon loaded from [unpkg.com](https://unpkg.com).
 *
 * **Usage:**
 * ```javascript
 * import { CDNFlagIcon } from "react-flag-kit";
 *
 * export function UsaFlag(props) {
 *   return <CDNFlagIcon {...props} code="US" />;
 * }
 * ```
 */
export function CDNFlagIcon({ code, ...props }) {
  const src = `https://unpkg.com/react-flag-kit/assets/${code}.svg`;

  return <Flag {...props} src={src} alt={code} />;
}

